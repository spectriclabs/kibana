import {
  PluginInitializerContext,
  CoreSetup,
  CoreStart,
  Plugin,
  Logger,
} from '../../../../src/core/server';

import { MapAnnotationsPluginSetup, MapAnnotationsPluginStart } from './types';
import { defineRoutes } from './routes';

export class MapAnnotationsPlugin
  implements Plugin<MapAnnotationsPluginSetup, MapAnnotationsPluginStart> {
  private readonly logger: Logger;

  constructor(initializerContext: PluginInitializerContext) {
    this.logger = initializerContext.logger.get();
  }

  public setup(core: CoreSetup) {
    this.logger.debug('map_annotations: Setup');
    const router = core.http.createRouter();

    // Register server side APIs
    defineRoutes(router);

    return {};
  }

  public start(core: CoreStart) {
    this.logger.debug('map_annotations: Started');
    return {};
  }

  public stop() {}
}
